/**
 * Enumeração EstadoAluno
 * 
 * Define os possíveis estados de um aluno no sistema académico.
 * 
 * @author [Taís Pestana]
 * @version 1.0
 */
public enum EstadoAluno {
    /** Aluno ativo e a frequentar o curso */
    ATIVO,
    /** Aluno temporariamente suspenso */
    SUSPENSO,
    /** Aluno que concluiu o curso com sucesso */
    CONCLUIDO,
    /** Aluno que desistiu do curso */
    DESISTENTE
}